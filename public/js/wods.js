function secondsToTime(rawSeconds) {
    var hours = Math.floor(rawSeconds / (60 * 60));

    var divisor_for_minutes = rawSeconds % (60 * 60);
    var minutes = Math.floor(divisor_for_minutes / 60);
    //minutes = (minutes > 0) ? minutes : '00';

    var divisor_for_seconds = divisor_for_minutes % 60;
    var seconds = Math.ceil(divisor_for_seconds);
    if(seconds < 10) {
        seconds = '0' + seconds;
    }

    return minutes + ':' + seconds;
}

function calculateTotalTime(WOD) {
    var exercisesWorkTime = (WOD.exerciseWork * WOD.exercises.length) * WOD.rounds;
    var exercisesRestTime = (WOD.exerciseRest * (WOD.exercises.length - 1)) * WOD.rounds;
    var roundsRestTime = (WOD.rounds - 1) * WOD.roundsRest;

    return exercisesWorkTime + exercisesRestTime + roundsRestTime;
}
